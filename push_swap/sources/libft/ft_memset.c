#include<stddef.h>

void	*ft_memset(void *src, int k, size_t n)
{
	char	*a;
	int		i;

	a = (char *)src;
	i = 0;
	while (i < (int)n)
	{
		a[i] = k;
		i++;
	}
	return (src);
}
